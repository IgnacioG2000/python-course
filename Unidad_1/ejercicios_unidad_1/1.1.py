# Cree un programa que tome tres valores por consola multiplique el primero
# por el segundo y le sume el tercero. Presente el resultado en la terminal


valor1 = int(input("Digita el primer numero: "))
valor2 = int(input("Digita el segundo numero: "))
valor3 = int(input("Digita el tercer numero: "))

resultado = valor1 * valor2 + valor3

print(f"El resultado es: {resultado}")
